    if (!aiConfig.openai) {
      console.warn('OpenAI n√£o dispon√≠vel para gera√ß√£o de conte√∫do BNCC');
      return content;
    }
    
    const bnccClassifier = createBNCCClassifier(aiConfig.openai);
